    private void cadastrar(){
            Proj010_Revisao p = new Proj010_Revisao();
        p.setNome(jtfNome.getText());
        
        p.setData_nasc(jtfData_nasc.getText());
        
        p.setCpf(jtfCPF.getText());
        
        p.setEndereco(jtfEndereco.getText());
        
        p.setFone(Integer.parseInt(jtfFone.getText()));
        
        p.setCurso(bgCurso.getSelection().getActionCommand());
        
        
        JOptionPane.showMessageDialog(
        rootPane,
                "CADASTRO REALIZADO COM SUCESSO"+
                "\nDados cadastrados!!"+ p.toString(),
                "\nSucesso",
                JOptionPane.PLAIN_MESSAGE
        );
}
    private void limpar(){
        jtfNome.setText(null);
        jtfEndereco.setText(null);
        jtfCPF.setText(null);
        jtfData_nasc.setText(null);
        jtfFone.setText(null);
    }
